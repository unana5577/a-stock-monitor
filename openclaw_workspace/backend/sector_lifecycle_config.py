def get_position_zone(alpha_20: float) -> str:
    """根据 Alpha_20 判断位置区域"""
    if alpha_20 >= ALPHA_THRESHOLDS["high"]:
        return "高位区"
    elif alpha_20 >= ALPHA_THRESHOLDS["mid_high"]:
        return "中位区"
    elif alpha_20 >= ALPHA_THRESHOLDS["mid_low"]:
        return "中位区"
    elif alpha_20 >= ALPHA_THRESHOLDS["low"]:
        return "低位区"
    else:
        return "异常区"


def get_combo_info(position_name: str, stage_signal: str) -> Tuple[str, str, str, str]:
    """
    获取位置阶段组合信息
    返回: (显示名称, 含义, 操作建议, 风险等级)
    """
    key = (position_name, stage_signal)
    if key in POSITION_STAGE_COMBOS:
        return POSITION_STAGE_COMBOS[key]
    else:
        return POSITION_STAGE_COMBOS[("default", "default")]


def get_factor_weights(market_state: str) -> Dict[str, float]:
    """获取不同市场状态下的因子权重"""
    return FACTOR_WEIGHTS.get(market_state, FACTOR_WEIGHTS["震荡"])


